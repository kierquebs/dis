	/**
	 * getQueryAddress function
	 *
	 * QUERY ACTION FOR COREPASS CLIENT
	 */
	public function getQueryAddress($addressID){
		/**
		 * 
		 */
		$result = $this->oracle_db->query("SELECT contactDataId, ADDRESS
				FROM
				(
					SELECT ECD.CONTACTDATA_ID AS contactDataId,
					EPCD.DOOR || EPCD.THOROUGHFARENAME || ' ' ||EPCD.THOROUGHFAREPOINT || ' ' ||	EMLS_ER1.DEFAULTVALUE || ' ' ||
					(SELECT defaultvalue FROM e_multi_language_string WHERE string_id=ER2.NAME_ID and ER2.REGIONTYPE_ID = 3) || ' ' ||
					(SELECT mls.defaultvalue FROM e_multi_language_string mls, E_REGION ER3 WHERE ER2.PARENT_ID = ER3.REGION_ID and mls.string_id=ER3.NAME_ID and ER3.REGIONTYPE_ID = 1) ADDRESS
					FROM 
					E_CONTACT_DATA ECD,
					E_POSTAL_CONTACT_DATA EPCD,
					E_THOROUGHFARE_TYPE TFT,
					E_REGION ER1,
					E_REGION ER2,
					E_MULTI_LANGUAGE_STRING EMLS_ER1,
					E_MULTI_LANGUAGE_STRING EMLS_TFT
					WHERE 
					ECD.CONTACTDATA_ID = EPCD.CONTACTDATA_ID
					AND EPCD.LOCALITY = ER1.REGION_ID
					AND ER1.NAME_ID = EMLS_ER1.STRING_ID
					AND ER1.PARENT_ID = ER2.REGION_ID
					AND EPCD.THOROUGHFARETYPE_ID = TFT.THOROUGHFARETYPE_ID
					AND TFT.NAME_ID = EMLS_TFT.STRING_ID
					AND ECD.CONTACTDATA_ID = ".$addressID."
				)
				GROUP BY contactDataId, ADDRESS
				ORDER BY contactDataId ASC");
		return $result;
	}

	/**
	 * getQueryAddress function
	 *
	 * QUERY ACTION FOR COREPASS CLIENT
	 */
	public function getQueryPeople($peopleID){
		/**
		 * 
		 */
		$result = $this->oracle_db->query("SELECT peopleId, ADDRESS
				FROM
				(
					SELECT distinct ECP.PEOPLE_ID peopleId,
					EPP.NAME || ' ' || EPP.lastname ContactPerson
					FROM 
					E_COMPANY_PERSON ECP,
					E_GROUP_PEOPLE EGP,  
					E_PHYSICAL_PERSON EPP
					WHERE 
					ECP.PEOPLE_ID = EGP.PEOPLE_ID
					AND EPP.PHYSICALPERSON_ID = ECP.PHYSICALPERSON_ID
					and ECP.PEOPLE_ID = ".$peopleID."
					ORDER BY ECP.PEOPLE_ID ASC
				)
				GROUP BY peopleId, ContactPerson
				ORDER BY peopleId ASC");
		return $result;
	}
	
