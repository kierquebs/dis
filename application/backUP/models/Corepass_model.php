	/**
	 * getQueryDeliveryPoint function
	 *
	 * QUERY ACTION FOR COREPASS AGREEMENT DELIVERY POINT
	 */
	public function getQueryDelPoint($agrID){
		if(empty($agrID)) return '';
		$result = $this->oracle_db->query("select 
			EDN2.DELIVERYNODE_ID AS deliveryPointsId,
			EDN2.DELIVERYNODENAME AS deliveryPointsName
			from
			E_DELIVERY_NODE EDN,
			E_DELIVERY_NODE EDN2,
			E_VOUCHER_CUST_AGREEMENT EVCA
			where 
			EVCA.DELIVERYNODE_ID = EDN.DELIVERYNODE_ID
			AND EDN.DELIVERYNODE_ID = EDN2.PARENT_ID
			AND EVCA.AGREEMENT_ID = $agrID");
		return $result;
	}
	
	/**
	 * getQueryAgrSpRole function
	 *
	 * QUERY ACTION FOR COREPASS AGREEMENT SPROLE ID
	 */
	public function getQueryAgrSpRole($agrID){
		if(empty($agrID)) return '';
		$result = $this->oracle_db->query("SELECT 
			EA.AGREEMENT_ID AGREEMENT_ID, SUB4_ar.AGREEMENTROLE_ID SPROLE_ID
			FROM
			E_COMPANY EC,
			E_AGREEMENT EA,
			E_AGREEMENT_ROLE_SET_AGR_ROLE SUB4_arsar,
			E_AGREEMENT_ROLE SUB4_ar
			,E_GROUP_PEOPLE SUB4_gp  
			,E_COMPANY_PERSON SUB4_cp 
			,E_PHYSICAL_PERSON SUB4_pp
			WHERE
			EC.COMPANY_ID = EA.COMPANY_ID
			AND EC.B_ACTIVE = 'T'
			AND EA.B_ACTIVE = 'T'
			and SUB4_AR.AGREEMENTROLETYPE_ID = 4
			AND SUB4_arsar.agreementroleset_id = EA.agreementroleset_id
			and SUB4_ARSAR.AGREEMENTROLE_ID=SUB4_AR.AGREEMENTROLE_ID
			and SUB4_AR.PEOPLE_ID=SUB4_GP.GROUP_ID
			and SUB4_GP.PEOPLE_ID=SUB4_CP.PEOPLE_ID
			and SUB4_PP.PHYSICALPERSON_ID=SUB4_CP.PHYSICALPERSON_ID
			and EA.AGREEMENT_ID = $agrID");
		return $result;
	}
	
